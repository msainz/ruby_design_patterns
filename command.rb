
# The Command pattern separates the thought from the deed, i.e. it separates creation time from execution
# time for a specific action.
# 
# Commands are useful for keeping a running list of things to be done, or remembering what has been done.
# They are useful to implement 'undo' functionality.
# 
# Commands may be implemented as full-scale classes or as simple code blocks.
# 
# Compared to Observer pattern, a command typically does not care about the state of the context that
# executed it. Conversely, an observer is intensely interested in the state of the subject that called it.

